- **Alexithymia** — a trait characterized by difficulty identifying and describing emotions, present in about 10% of the population
- **Emotional numbing** — a common response to chronic stress or trauma
- **Cultural conditioning** — some cultural environments discourage emotional expression and limit the emotional vocabulary available to children

If this resonates with you, know that emotional granularity can be developed at any age. Start with body sensations (easier to detect) and gradually build toward emotion labels.'
