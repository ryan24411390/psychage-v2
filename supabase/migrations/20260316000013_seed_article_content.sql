-- CAT01-007: Feelings vs Emotions vs Moods
UPDATE public.articles SET content = '## Feelings vs. Emotions vs. Moods: What''s the Difference?

We use the words "feelings," "emotions," and "moods" interchangeably in everyday conversation. But in psychology, they refer to distinct phenomena — and understanding the differences can help you navigate your inner world more effectively.

### Emotions: Brief, Intense, and Targeted

An **emotion** is a rapid, coordinated response to a specific event or stimulus. Emotions are:

- **Brief** — typically lasting seconds to minutes
- **Intense** — they grab your attention
- **Object-directed** — they are *about* something (you''re angry *at* someone, afraid *of* something)
- **Multi-component** — they involve simultaneous changes in your body, behavior, and subjective experience

When a car swerves toward you, the flash of fear you experience is an emotion. It has a clear trigger, produces immediate physiological changes (adrenaline surge, muscle tension), and motivates a specific action (swerving, braking).

### Feelings: The Subjective Layer

A **feeling** is your conscious, subjective experience of an emotion. It''s the "what it''s like" aspect — the part you can reflect on and describe in words.

The distinction matters because:
- You can have an emotional response *without* a conscious feeling (your body reacts before you realize it)
- Feelings require a level of self-awareness that emotions don''t
- Two people can have the same emotional response but different feelings about it (one person might feel ashamed of their anger; another might feel justified)

Neuroscientist Antonio Damasio describes this as the difference between an emotion (a body state) and a feeling (the mental representation of that body state). Emotions happen *to* your body; feelings happen *in* your mind.

### Moods: Low-Grade and Pervasive

A **mood** is a diffuse, longer-lasting affective state that colors your overall experience without being directed at any specific object. Moods are:

- **Extended** — lasting hours, days, or even weeks
- **Low intensity** — they operate in the background rather than demanding attention
- **Non-specific** — they aren''t *about* anything in particular
- **Tone-setting** — they influence how you interpret events and which emotions you''re likely to experience

If you wake up feeling irritable for no clear reason, that''s a mood. If someone''s comment then makes you furious (when it normally wouldn''t), your mood *lowered the threshold* for that specific emotion.

### How They Interact

These three layers are deeply interconnected:

1. **Moods bias emotions**: An anxious mood makes you more likely to interpret ambiguous situations as threatening, triggering fear emotions you wouldn''t otherwise experience
2. **Repeated emotions shape moods**: Experiencing the same emotion repeatedly (e.g., frustration at work) can create a sustained mood (chronic irritability)
3. **Feelings feed back**: How you feel about your emotions (meta-emotions) influences future emotional responses — feeling guilty about anger, for instance, can create a cycle of suppression

### Practical Applications

Understanding these distinctions helps you respond more effectively:

**For emotions** (brief, intense):
- Use in-the-moment techniques: deep breathing, grounding, labeling
- Let them pass — they''re designed to be temporary

**For feelings** (subjective interpretations):
- Examine the story you''re telling yourself about the emotion
- Practice cognitive reappraisal: "Is there another way to interpret what I''m feeling?"

**For moods** (pervasive background states):
- Address the underlying causes: sleep, nutrition, exercise, social connection
- Track mood patterns over time to identify triggers
- Consider whether a persistent low mood might indicate a clinical condition worth discussing with a professional

### The Integrated View

Rather than seeing emotions, feelings, and moods as completely separate, think of them as different *scales* of the same underlying process — like weather and climate. An emotion is a thunderstorm (brief, dramatic, localized). A mood is a weather pattern (lasting days, covering a wide area). And feelings are your experience of standing in the rain.'
